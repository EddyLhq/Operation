#!/bin/bash
set -e
SCRIPT_PATH="$(cd "$(dirname "$0")" && pwd)/$(basename "$0")"
RED='\033[0;31m'; GREEN='\033[0;32m'; BLUE='\033[0;34m'; NC='\033[0m'
INSTALL_DIR="/opt/openvpn-manager"
print_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
print_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
print_section() { echo ""; echo "============================================================"; echo -e "${GREEN}$1${NC}"; echo "============================================================"; }
check_root() { if [ "$EUID" -ne 0 ]; then echo -e "${RED}[ERROR] Run as root${NC}"; exit 1; fi; }

configure_apt_mirrors() {
    print_section "Configuring APT Mirrors"
    if [ ! -f /etc/apt/sources.list.bak ]; then cp /etc/apt/sources.list /etc/apt/sources.list.bak; fi
    if timeout 3 curl -s --head https://mirrors.aliyun.com > /dev/null 2>&1; then
        print_info "Configuring Aliyun mirrors..."
        cat > /etc/apt/sources.list << 'EOF'
deb http://mirrors.aliyun.com/ubuntu/ jammy main restricted universe multiverse
deb http://mirrors.aliyun.com/ubuntu/ jammy-security main restricted universe multiverse
deb http://mirrors.aliyun.com/ubuntu/ jammy-updates main restricted universe multiverse
deb http://mirrors.aliyun.com/ubuntu/ jammy-backports main restricted universe multiverse
EOF
    fi
    apt-get update -qq; print_success "APT updated"
}

install_dependencies() {
    print_section "Installing Dependencies"; export DEBIAN_FRONTEND=noninteractive
    apt-get install -y -qq openvpn easy-rsa python3 python3-pip curl wget net-tools iptables
    mkdir -p ~/.pip; cat > ~/.pip/pip.conf << 'EOF'
[global]
index-url = https://pypi.tuna.tsinghua.edu.cn/simple
trusted-host = pypi.tuna.tsinghua.edu.cn
EOF
    pip3 install -q Flask pyotp qrcode Pillow; print_success "Dependencies installed"
}

configure_network() {
    print_section "Configuring Network Forwarding"
    echo 1 > /proc/sys/net/ipv4/ip_forward
    if ! grep -q "^net.ipv4.ip_forward=1" /etc/sysctl.conf; then
        echo "net.ipv4.ip_forward=1" >> /etc/sysctl.conf
    fi
    sysctl -p > /dev/null 2>&1 || true
    DEFAULT_IF=$(ip route | grep default | awk '{print $5}' | head -1)
    if [ -n "$DEFAULT_IF" ]; then
        if ! iptables -t nat -C POSTROUTING -o "$DEFAULT_IF" -j MASQUERADE 2>/dev/null; then
            iptables -t nat -A POSTROUTING -o "$DEFAULT_IF" -j MASQUERADE
        fi
        mkdir -p /etc/iptables
        iptables-save > /etc/iptables/rules.v4
        cat > /etc/systemd/system/iptables-restore.service << EOF
[Unit]
Description=Restore iptables rules
Before=network-pre.target
Wants=network-pre.target
[Service]
Type=oneshot
ExecStart=/sbin/iptables-restore /etc/iptables/rules.v4
[Install]
WantedBy=multi-user.target
EOF
        systemctl daemon-reload
        systemctl enable iptables-restore > /dev/null 2>&1
    else
        iptables -t nat -A POSTROUTING -j MASQUERADE
    fi
    print_success "Network forwarding configured"
}

setup_dirs() {
    print_section "Creating Directories"
    mkdir -p "$INSTALL_DIR"/{data/{ovpn_files,qr_codes},static/{css,js},templates,services}
    mkdir -p /etc/openvpn/ccd /var/log/openvpn /var/run/openvpn
    if [ ! -d /etc/openvpn/easy-rsa ]; then cp -r /usr/share/easy-rsa /etc/openvpn/easy-rsa; chmod +x /etc/openvpn/easy-rsa/easyrsa; fi
    echo "[]" > "$INSTALL_DIR/data/clients.json"; echo "{}" > "$INSTALL_DIR/data/server_config.json"
    touch /etc/openvpn/user_passwords; chmod 644 /etc/openvpn/user_passwords
    echo "{}" > /etc/openvpn/mfa_secrets.json; chmod 644 /etc/openvpn/mfa_secrets.json
    print_success "Directories created"
}

extract_project() {
    print_section "Extracting Project"; cd "$INSTALL_DIR"
    ARCHIVE_START=$(awk '/^__ARCHIVE_START__/{print NR + 1; exit}' "$SCRIPT_PATH")
    tail -n+$ARCHIVE_START "$SCRIPT_PATH" | base64 -d | tar -xzf -
    echo "[]" > "$INSTALL_DIR/data/clients.json"; echo "{}" > "$INSTALL_DIR/data/server_config.json"
    print_success "Project extracted"
}

create_service() {
    print_section "Creating Systemd Service"
    cat > /etc/systemd/system/openvpn-manager.service << 'EOF'
[Unit]
Description=OpenVPN Manager
After=network.target
[Service]
Type=simple
User=root
WorkingDirectory=/opt/openvpn-manager
ExecStart=/usr/bin/python3 /opt/openvpn-manager/app.py
Restart=always
[Install]
WantedBy=multi-user.target
EOF
    systemctl daemon-reload; systemctl enable openvpn-manager; print_success "Service created"
}

main() {
    check_root
    echo -e "\n${GREEN}============================================================${NC}"
    echo -e "${GREEN}OpenVPN Manager - Automated Deployment${NC}"
    echo -e "${GREEN}============================================================${NC}\n"
    configure_apt_mirrors
    install_dependencies
    configure_network
    setup_dirs
    extract_project
    create_service
    systemctl start openvpn-manager
    
    print_section "Deployment Complete!"
    echo "Web: http://$(hostname -I | awk '{print $1}'):5000"
    echo "Login: admin / Admin@2025"
    echo ""
    echo "Features:"
    echo "  - IP forwarding: enabled"
    echo "  - NAT masquerading: enabled"
    echo "  - Access control: VPN clients restricted to configured business networks"
    echo ""
    echo "Next: Initialize PKI -> Configure server -> Start OpenVPN -> Create clients"
    echo ""
}
main; exit 0
